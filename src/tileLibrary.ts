export const TILE_LIBRARY: TileLibraryEntry[] = RAW
  .map(([brand, name, pixelWidth, pixelHeight, powerWatts]) => ({ brand, name, pixelWidth, pixelHeight, powerWatts }))
  .sort((a, b) => {
    const brandCmp = a.brand.localeCompare(b.brand);
    return brandCmp !== 0 ? brandCmp : a.name.localeCompare(b.name);
  });

/** All unique brand names, alphabetically. */
export const TILE_BRANDS: string[] = [...new Set(TILE_LIBRARY.map((t) => t.brand))].sort();

/** Filter by search term (brand or name, case-insensitive). */
export function searchTileLibrary(query: string): TileLibraryEntry[] {
  const q = query.toLowerCase().trim();
  if (!q) return TILE_LIBRARY;
  return TILE_LIBRARY.filter(
    (t) => t.brand.toLowerCase().includes(q) || t.name.toLowerCase().includes(q),
  );
}

/** Group entries by brand for display. */
export function groupedTileLibrary(entries: TileLibraryEntry[] = TILE_LIBRARY): Record<string, TileLibraryEntry[]> {
  const groups: Record<string, TileLibraryEntry[]> = {};
  for (const entry of entries) {
    (groups[entry.brand] ??= []).push(entry);
  }
  return groups;
}
